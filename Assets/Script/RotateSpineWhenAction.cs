    // OnStateExit is called when a transition ends and the state machine finishes evaluating this state
    override public void OnStateExit(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        AnimatorStateInfo nextState = animator.GetNextAnimatorStateInfo(layerIndex);

        // 다음 상태가 여전히 공격 계열이라면 RotSpine 유지
        if (nextState.IsName("Attack2"))  // 필요 시 "Base Layer.AttackCombo.Attack2" 등 전체 경로
        {
            return;
        }

        animator.SetBool("RotSpine", false);
